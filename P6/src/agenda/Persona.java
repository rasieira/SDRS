package agenda;

public class Persona
{
	private String nombre;
	private String tfno;
	
	public Persona(){ }

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getTfno() {
		return tfno;
	}

	public void setTfno(String tfno) {
		this.tfno = tfno;
	};
}
